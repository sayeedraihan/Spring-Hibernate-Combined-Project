package com.example.springhibernate.model;

public class Department {
    private String id;
    private String name;

    public String getId() {
        return id;
    }
    public void setId(String _id) {
        id = _id;
    }
    public String getName() {
        return name;
    }
    public void setName(String _name) {
        name = _name;
    }
}
